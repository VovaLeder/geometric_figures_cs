namespace GeomFigures
{
    /// <summary>
    /// Class <c>Figure</c> models an abstract figure that has area that can be calculated
    /// </summary>
    public abstract class Figure
    {
        /// <summary>
        /// Method <c>GetArea</c> calculates area of a figure
        /// </summary>
        /// <returns>Area of a figure</returns>
        public abstract double GetArea();
    }
}
